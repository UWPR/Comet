         <h2>Comet MS/MS</h2>


         <p>Searching uninterpreted tandem mass spectra of peptides against sequence databases
         is the most common method used to identify peptides and proteins.  Since this method
         was first developed in 1993, many commercial, free, and open source tools have been
         created over the years that accomplish this task.</p>

         <p>Although its history goes back two decades, the Comet search engine was first
         made publicly available in August 2012 on SourceForge under the
         <a target="new" href="http://www.apache.org/licenses/LICENSE-2.0.html">Apache License, version 2.0</A>.
         Comet is multithreaded, supports multiple input and output formats, and binaries
         are available for both Windows and Linux operating systems.

         <p>Note that Comet is just a single command line binary that does MS/MS database
         search.  It takes in spectra in some supported input format and writes out
         .pep.xml, .pin.xml, .sqt and/or .out files. You <i>will</i> need some other support
         tool(s) to actually make use of Comet results.


