         <h1>Comet UI</h1>
      </div>
      <div class="post hr">
         <p>This is a 64-bit Windows graphical interface to run Comet searches
         and visualize results at the PSM level.  This is just the initial
         release so full search functionality exists but there is limited
         post-search analysis available.  The GUI can also load data processed
         through PeptideProphet of the TPP.  This software will be further
         developed if there is demand for it.

         <p>Thermo's MS File Reader is required if you want to search RAW
         files directly.  MSFileReader can be downloaded by creating an account
         with <a href="https://thermo.flexnetoperations.com/control/thmo/login">ThermoFischer Scientific<a/>. Once registered you will find the software under "Utility Software".



         <p><b>NOTE: this first release should be viewed as beta software as
         features are incomplete and not well tested.
         <br>If you are running version 1.0.0.0, please download the setup.exe
         again as the initial version was not automatically checking for updates.</b>

         <p>Install the program using this <a href="setup.exe" onClick="ga('send', 'event', 'CometUI', 'Download', 'setup.exe');" >setup.exe</a>.

         <p>A work-in-progress user guide can be found <a href="CometUI-User-Guide.pdf" onClick="ga('send', 'event', 'CometUI', 'Download', 'user_guide');" >here</a>.

         <p>01/07/2016:  CometUI updated to support reading Tide pep.xml files.
